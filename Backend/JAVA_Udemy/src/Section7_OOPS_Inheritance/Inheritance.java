package Section7_OOPS_Inheritance;

public class Inheritance {
	
	// Inheritance is a mechanism in which one object acquires all the properties and behaviors of a parent object.
	// It represents IS-A relationship.
	// It is used for code reusability and method overriding.
	// Inheritance can be of 5 types:
	// 1. Single Inheritance: A class is allowed to inherit from only one class.
	// 2. Multilevel Inheritance: A class is derived from another derived class.
	// 3. Hierarchical Inheritance: Multiple classes are derived from a single base class.
	// 4. Multiple Inheritance: A class is derived from more than one class.
	// 5. Hybrid Inheritance: A combination of two or more types of inheritance.


}

